//pushing even nums to array
var evenNums=[];
for(var i=0;i<10;i+=2){
    evenNums.push(i);
}
console.log(evenNums);

//pushing odd nums to array
var oddNums=[];
for(var i=1;i<10;i+=2){
    oddNums.push(i);
}
console.log(oddNums);

//counting backwards
var countBack=[];
for(var i=10;i>0;i--){
    countBack.push(i);
}
console.log(countBack);

